    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:

            File save = new File(jTextField1.getText() + ".jpeg");

            switch (teste) {
                case 1:
                    if (!save.exists()) {
                        try {
                            ChartUtilities.saveChartAsJPEG(save, xy, 400, 600);
                        } catch (IOException ex) {
                            Logger.getLogger(EstatisticaJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    break;

                case 2:
                    if (!save.exists()) {
                        try {
                            ChartUtilities.saveChartAsJPEG(save, pie, 400, 600);
                        } catch (IOException ex) {
                            Logger.getLogger(EstatisticaJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    break;
                    
                    case 3:
                    if (!save.exists()) {
                        try {
                            ChartUtilities.saveChartAsJPEG(save, sp, 400, 600);
                        } catch (IOException ex) {
                            Logger.getLogger(EstatisticaJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    break;
                    
                    case 4:
                    if (!save.exists()) {
                        try {
                            ChartUtilities.saveChartAsJPEG(save, bar2d, 400, 600);
                        } catch (IOException ex) {
                            Logger.getLogger(EstatisticaJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    break;
                    
                    case 5:
                    if (!save.exists()) {
                        try {
                            ChartUtilities.saveChartAsJPEG(save, bar3d, 400, 600);
                        } catch (IOException ex) {
                            Logger.getLogger(EstatisticaJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    break;
            }

            JOptionPane.showMessageDialog(null, "Gráfico salvo com sucesso!", "Sucesso!", JOptionPane.OK_OPTION);
        

    }//GEN-LAST:event_saveButtonActionPerformed

