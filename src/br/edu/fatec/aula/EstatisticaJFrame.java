    private void barra3dButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barra3dButtonActionPerformed

        teste = 5;
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        for (int i = 0; i < model.getSize(); i++) {
            String numStr = String.valueOf(model.get(i));
            Double num = Double.parseDouble(numStr);
            dataset.addValue(num, "Numero", "" + (i + 1));
        }

        dataset.addValue(20, "Indice", "20");

         bar3d = ChartFactory.createBarChart3D("3D Bar",
                "Índices",
                "Valores",
                dataset,
                PlotOrientation.VERTICAL,
                true,
                true,
                false);

        // PARTE PADRÃO DE TODOS OS GRÁFICOS 
        ChartPanel panel = new ChartPanel(bar3d);
        int width = graphicsPanel.getWidth();
        int height = graphicsPanel.getHeight();

        panel.setPreferredSize(new Dimension(width, height));

        graphicsPanel.setLayout(new BorderLayout());
        graphicsPanel.add(panel, BorderLayout.CENTER);
        graphicsPanel.validate();

    }//GEN-LAST:event_barra3dButtonActionPerformed

